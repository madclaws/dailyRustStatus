fn main() {
    println!("MiniStringFuck Interpreter!");
    my_first_interpreter("+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++.+.+.+.+.+.+.+.+.+.+.+.+.+.+.+.+.+.+.+.+.+.+.+.+.+.");
}

fn my_first_interpreter(code: &str) -> String {
    let mut interpreted: String = String::from("");
    let mut memory: u8 = 0;
    
    for symbol in code.chars() {
        if symbol == '+' {
            memory = memory.checked_add(1).unwrap_or(0);
        } else if symbol == '.' {
            interpreted.push(memory as char);
        } else {
            continue;
        }
    }
    println!("{}", interpreted);
    interpreted
}

